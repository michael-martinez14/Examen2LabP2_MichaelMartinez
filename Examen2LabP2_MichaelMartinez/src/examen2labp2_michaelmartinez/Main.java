    private void jb_refreshMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jb_refreshMouseClicked
        // TODO add your handling code here:
        AdminCarros admin=new AdminCarros("./ListaCarros.tn");
        admin.leer();
        //modelos de comboBox
        DefaultComboBoxModel modelo1=(DefaultComboBoxModel)cb_carro1.getModel();
        DefaultComboBoxModel modelo2=(DefaultComboBoxModel)cb_carro2.getModel();
        modelo1.removeAllElements();
        modelo2.removeAllElements();
        for (int i = 0; i < admin.getListaCarros().size(); i++) {
            modelo1.addElement(admin.getListaCarros().get(i));
            modelo2.addElement(admin.getListaCarros().get(i));
        }
        cb_carro1.setModel(modelo1);
        cb_carro2.setModel(modelo2);
    }//GEN-LAST:event_jb_refreshMouseClicked

