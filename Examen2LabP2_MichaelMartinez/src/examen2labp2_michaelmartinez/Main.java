    private void jb_crearCarroNuevoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jb_crearCarroNuevoMouseClicked
        try {
            // TODO add your handling code here:
            AdminCarros admin=new AdminCarros("./ListaCarros.tn");
            admin.leer();
            if (tf_marca.getText().equals("")|| tf_modelo.getText().equals("")) {
                JOptionPane.showMessageDialog(jd_registarAuto, "Hay un campo vacío");
            } else {
                Carro carroNuevo = new Carro(tf_marca.getText(), tf_modelo.getText(), Integer.parseInt(js_velocidad.getValue() + ""));
                admin.getListaCarros().add(carroNuevo);
                JOptionPane.showMessageDialog(jd_registarAuto, "Carro creado exitosamente");
                admin.escribir();
                tf_marca.setText("");
                tf_modelo.setText("");
                js_velocidad.setValue(0);
            }
            
        } catch (IOException ex) {
            ex.printStackTrace();
        }

    }//GEN-LAST:event_jb_crearCarroNuevoMouseClicked

